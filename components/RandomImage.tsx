interface RandomImageProps {
  width: number;
  height: number;
  className?: string;
  alt?: string;
}

export function RandomImage({
  width,
  height,
  className,
  alt = "Recipe",
}: RandomImageProps) {
  // Use useMemo to keep the same random image ID for the lifetime of the component
  const randomImageId = useMemo(() => {
    const randomIndex = Math.floor(Math.random() * imageIds.length);
    return imageIds[randomIndex];
  }, []);

  return (
    <Image
      src={`/images/random/${randomImageId}.jpg`}
      alt={alt}
      width={width}
      height={height}
      className={className}
    />
  );
}
